    static void initBiomeAdd(ID id, TypeCat type_cat, TempCat temp_cat, float temperature, float elevation) {
        int32_t idx = static_cast<int>(id);
        if (idx & (~0xff)) {
            return;
        } else {
            biomes[idx].id = id;
            biomes[idx].temp_cat = temp_cat;
            biomes[idx].type_cat = type_cat;
            biomes[idx].elevation = elevation;
            biomes[idx].temperature = temperature;
        }
    }
    
    static void initBiomeMutation(ID id) {
        int32_t idx = static_cast<int>(id);
        biomes[idx+128] = biomes[idx];
        biomes[idx+128].id = ID(idx+128);
    }
    
